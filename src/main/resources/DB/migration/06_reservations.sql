-- 8. 데이터 확인
SELECT
    U_ID,
    COUNT(*) as 예약수,
    SUM(CASE WHEN RESERVATION_STATUS = '예약완료' THEN 1 ELSE 0 END) as 완료,
    SUM(CASE WHEN RESERVATION_STATUS = '예약취소' THEN 1 ELSE 0 END) as 취소,
    SUM(TOTAL_AMOUNT) as 총금액
FROM RESERVATIONS
GROUP BY U_ID
ORDER BY U_ID;

